	function menu_4(id){ 
    var i;	
    for(i=1;i<=3;i++){
       var licw=document.getElementById("mod"+i);
        var yao=document.getElementById("lay"+i);
		if (i==id){ 
            licw.style.display = "block";
			yao.className="current2";
        }
		else		 
		{ 	
		licw.style.display = "none";
		yao.className="";
		}
		
    } 	
} 


